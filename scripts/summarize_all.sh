#!/bin/bash

# ==============================================================================
# 汇总所有实验组 (Exp 1 - 7) 的结果
# 该脚本会自动调用 tools/summarize.py 对每一组实验生成对比图和汇总表。
# ==============================================================================

# 进入项目根目录
cd "$(dirname "$0")/.." || exit

echo "--- 开始汇总所有实验结果 (Exp 1 - 7) ---"

for i in {1..7}; do
    GROUP="exp$i"
    echo -e "\n>>> 正在汇总实验组: ${GROUP} <<<"
    
    # 执行汇总工具
    # 注意：工具内部已经处理了输出路径隔离 (results/expX_summarize)
    python tools/summarize.py --group "$GROUP"
done

echo -e "\n--- 所有实验汇总完毕！结果存储在 results/ 目录下的各 summarize 文件夹中。 ---"
